def scan_ports(host, ports=None, threads=100, timeout=3, progress_callback=None):
    if ports is None:
        ports = COMMON_PORTS
    open_ports = []
    lock = threading.Lock()
    total = len(ports)
    done = [0]

    def worker(port_chunk):
        local_open = []
        for port in port_chunk:
            try:
                with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
                    sock.settimeout(timeout)
                    result = sock.connect_ex((host, port))
                    if result == 0:
                        service = SERVICE_MAP.get(port, "")
                        local_open.append({"port": port, "service": service})
            except Exception:
                pass
            with lock:
                done[0] += 1
                if progress_callback and done[0] % 20 == 0:
                    progress_callback(done[0], total)
        with lock:
            open_ports.extend(local_open)

    chunk_size = max(1, len(ports) // threads)
    chunks = [ports[i:i + chunk_size] for i in range(0, len(ports), chunk_size)]
    thread_list = []
    for chunk in chunks:
        t = threading.Thread(target=lambda c=chunk: worker(c))
        t.start()
        thread_list.append(t)
    for t in thread_list:
        t.join()

    if progress_callback:
        progress_callback(total, total)

    open_ports.sort(key=lambda x: x["port"])
    return open_ports


